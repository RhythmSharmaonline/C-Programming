#include <stdio.h>

int main()
{
    int a = 2;
    int b = 3;

    // Arithmetic operators.

    // + (Addition)
    // - (Substraction)
    // * (Multiplication)
    // / (Division)
    // % (Modulus)
    // ++ (Increament)
    // -- (Decrement)

    printf("Addition=%d\n", a + b);
    printf("Substraction=%d\n", a - b);
    printf("Multiplication=%d\n", a * b);
    printf("Modulus=%d\n", a % b);
    printf("Increament=%d\n", a++);
    printf("Decrement=%d\n", a--);
    printf("Value=%d", -a);

    return 0;
}
